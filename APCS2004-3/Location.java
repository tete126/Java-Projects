public class Location
  {
    private int row;
    private int col;

    public Location(int row, int col)
    {
      this.row = row;
      this.col = col;
    }

    public void setRow(int r)
    {
      this.row = r;
    }

    public void setCol(int c)
    {
      this.col = c;
    }

    public int getRow()
    {
      return row;
    }

    public int getCol()
    {
      return col;
    }
  }


